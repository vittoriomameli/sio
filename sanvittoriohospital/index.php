        <h5 class="w3-padding-32 w3-center">DAL 1986 AL TUO SERVIZIO</h5>
        <p class="w3-text-black w3-justify">Coniugando l’esperienza clinica con l’eccellenza delle attività di ricerca e didattica universitaria, il San Vittorio Hospital intende affermarsi quale “promotore di salute” presso la comunità dei cittadini costituendosi, in particolare, quale centro ospedaliero di riferimento per lo studio, la diagnosi e la cura nelle seguenti alte specialità cliniche:

          clinica dell'emergenza con attenzione alle patologie cardiovascolari, neurovascolari e nefro-urologiche
          clinica oncologica
          chirurgia d’alta complessità clinico-tecnologica
          medicina specialistica con centri diagnostici di eccellenza tecnologica e clinica

          L’Azienda Ospedaliera San Vittorio Hospital promuove, con riferimento alla propria Carta dei Valori, una nuova cultura individuale e sociale del benessere e della salute adoperandosi in favore dell’umanizzazione dei servizi e delle strutture ospedaliere.

          L’attività didattica universitaria presso il San Vittorio Hospital intende formare operatori clinici che coniughino l’eccellenza scientifica con le competenze professionali ed i valori etici appresi nell’ambito del tirocinio ospedaliero aziendale.<br><br>
          <h5 class="w3-padding-32 w3-center">PREMIO ALL'ECCELLENZA IN SANITA'</h5>
          <p class="w3-padding-32 w3-justify"> Il Premio s’inserisce nell’ambito delle attività finalizzate alla promozione ed allo sviluppo della cultura della qualità, nel Sistema Sanitario Regionale. L’iniziativa, utile alla diffusione di progetti di miglioramento, nasce in risposta all’esigenza di creare presupposti metodologici per l’implementazione di strategie, sistemi gestionali e progetti orientati alla qualità attraverso l’integrazione di modelli teorici e strumenti in grado di favorire un cambiamento evolutivo.
            Titolo del progetto: “Il process management come strumento di  gestione per affrontare una problematiche di rilevanza sociale: le lesioni da pressione”

            Titolo del progetto: “la gestione del rischio nel processo terapeutico: la scheda di terapia integrata”


            3 BOLLINI ROSA DELL'O.N.D.A. 
            L’Osservatorio Nazionale sulla salute della Donna (O.N.Da) ha ideato un programma speciale di segnalazione degli ospedali basato sul loro livello di “women friendship”, cioè sul grado di attenzione posta non solo nei confronti dei campi della medicina dedicati alle patologie femminili, ma anche verso le esigenze specifiche delle donne ricoverate.
