            SISTEMA DI ACCREDITAMENTO ALL'ECCELLENZA 
            In linea con la filosofia della qualità, l’A.O. San Vittorio Hospital pone la persona al centro della propria attività. Per questo motivo la struttura ha sviluppato il Sistema di Gestione Aziendale per la Qualità, implementando percorsi di Accreditamento all’eccellenza.  Le attività implementate sono la conferma che l'Azienda sta applicando un modello di organizzazione del lavoro in grado di offrire ai pazienti sempre maggiori garanzie in termini di efficacia e appropriatezza delle prestazioni ricevute e, in generale, di rispondere adeguatamente a tutte le loro esigenze. 
            In fase di completamento il processo di accreditamento all’eccellenza per la Breast Unit secondo quanto previsto dall’ EUSOMA (European Society of Breast Cancer Specialists)
